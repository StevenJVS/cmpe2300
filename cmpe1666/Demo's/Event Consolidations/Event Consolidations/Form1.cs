using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Data;
using System.Drawing;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Forms;

namespace Event_Consolidations
{
    public partial class Form1 : Form
    {
        public Form1()
        {
            InitializeComponent();
        }

        private void UI_Radio_Click(object sender, EventArgs e)
        {
            if (UI_red.Checked)
            {
                UI_readonly.BackColor = Color.Red;
            }
            else if (UI_green.Checked)
            {
                UI_readonly.BackColor = Color.Green;
            }
            else if (UI_blue.Checked)
            {
                UI_readonly.BackColor = Color.Blue;
            }

        }
    }
}
